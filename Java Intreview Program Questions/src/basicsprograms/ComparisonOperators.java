package basicsprograms;
//Comparison / Relational Operators----> ==, >, <, >=, <=, !=

public class ComparisonOperators 
{
	public static void main(String[] args) 
	{
		int a=10,b=5;
		System.out.println(a==b);
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a!=b);
	}

}
